__global__ void detect_edges(unsigned char *in, unsigned char *out) {
  int i = (blockIdx.x * 72) + threadIdx.x;
  int y, w; // the pixel of interest
  int s, h, u, b; // the pixels adjacent to x,y used for the calculation
  int o; // the result of calculate

    w = i / width;
    y = i - (width * w);

    if (y == 0 || w == 0 || y == width - 1 || w == height - 1) {
      out[i] = 0;
    } else {
      s = i + width;
      h = i - 1;
      u = i + 1;
      b = i - width;

      o = (in[i] * 4) + (in[s] * -1) + (in[h] * -1) + (in[u] * -1)
          + (in[b] * -1);

      if (o > 0) { // if the result is positive this is an edge pixel
        out[i] = 255;
      } else {
        out[i] = 0;
      }
    }
  }


